class Cacl:
    def add(self, a, b):
        """
        +法
        :param a: 参数1
        :param b: 参数2
        :return: 参数1+参数2
        """
        return a + b

    def minus(self, a, b):
        """
        -法
        :param a: 参数1
        :param b: 参数2
        :return: 参数1+参数2
        """
        return a - b

    def product(self, a, b):
        """
        *法
        :param a: 参数1
        :param b: 参数2
        :return: 参数1+参数2
        """
        return a * b

    def div(self, a, b):
        """
        /法
        :param a: 参数1
        :param b: 参数2
        :return: 参数1+参数2
        """
        return a / b


